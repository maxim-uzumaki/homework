// *Задача 3.
// Выведите в консоль шахматную доску размером 8x8, где символы чередуются (например, # и пробел). Начинайте с # в первой ячейке первой строки.

let board = "";

for (let i = 1; i <= 8; i++) {
	if (i % 2 === 1) {
		for (let j = 1; j <= 8; j++) {
			if (j % 2 === 1) {
				board += "X ";
			} else {
				board += "O ";
			}
		}
	} else {
		for (let j = 1; j <= 8; j++) {
			if (j % 2 === 1) {
				board += "O ";
			} else {
				board += "X ";
			}
		}
	}
	board += "\n";
}

console.log(board);
