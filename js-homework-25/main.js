// Задача 2.
// С помощью цикла for вычислите факториал числа (произведение всех чисел от 1 до заданного числа). Используйте prompt для получение начального значения.

console.log("Задача 2.");

let number = +prompt("Введите число, чтобы вычислить его факториал:", 1);
let factorial = 1;

for (let i = 1; i <= number; i++) {
	factorial *= i;
}

console.log(`Факториал числа ${number} равен ${factorial}`);

